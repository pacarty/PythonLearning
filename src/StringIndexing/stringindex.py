# We can get an individual character from a string
# by indexing it.

name = "Patrick"

# eg. char in position 0
print(name[0])

# eg. char in position 2
print(name[2])

# using a negative index takes chars from the end.
# -1 means the last char in the string,
# (because -0 is the same as 0).
print(name[-1])